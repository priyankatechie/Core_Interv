package core_interview;

// Function to Find the Larger of Two Numbers

public class FunctiontoFindtheLargerofTwoNumbers {

	public static int largest(int a ,  int b) {
		
		return (a>b) ? a:b;
	}
	
	
	
	public static void main(String args[]) {
		
		System.out.println(largest(10,20));
	}
	
	
}
